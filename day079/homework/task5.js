// 6) შექმენით მასივი nums = [5, 10, 15, 20]. for of ციკლით კონსოლში დაბეჭდეთ თითოეული რიცხვი. კომენტარებით ახსენით რა განსხვავებაა for in და for of შორის
let nums = [5, 10, 15, 20]
for ( i of nums){
    console.log(`${i}: ${nums[i]}`)
}
// for in ნახულობს გასაღებს(key)
// for of ნახულობს მნიშვნელობას (value)