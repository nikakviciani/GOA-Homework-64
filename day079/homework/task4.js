// 5) შექმენით ობიექტი user = {name: "Luka", level: 5, score: 190}. გამოიყენეთ for in ციკლი რომ გადაიაროთ ყველა key ამ ობიექტში და კონსოლში დაბეჭდოთ `${key}: ${user[key]}`. კომენტარებით ახსენით რისთვის გამოიყენება for in
let user = {name: "Luka", level: 5, score: 190};
for (let key in user) {
    // for in ციკლი გამოიყენება ობიექტის ყველა key-ს (საკუთრების სახელს) გასავლელად
    console.log(`${key}: ${user[key]}`); // აქ ჩვენ ვბეჭდავთ key-ს და მის შესაბამის მნიშვნელობას
}