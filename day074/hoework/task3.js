// 4) შექმენით countdown ტაიმერი, სადაც ცვლადი time = 10 დაიკლებს ყოველ წამში ერთით. როდესაც მნიშვნელობა გახდება 0, უნდა გაჩერდეს ინტერვალი clearInterval ფუნქციით და კონსოლში გამოიტანოს ტექსტი "Time up". კომენტარებით დაწერეთ რას აკეთებს clearInterval
let time = 10; // საწყისი დრო 10 წამი

let countdownInterval = setInterval(function() {
    console.log(time); // კონსოლში გამოიტანეთ მიმდინარე დრო
    time--; // დრო დაიკლებს ერთით
    if (time < 0) {
        clearInterval(countdownInterval); // clearInterval ფუნქცია აჩერებს ინტერვალს, რომელიც შექმნილია setInterval-ით
        console.log("Time up"); // კონსოლში გამოიტანეთ ტექსტი "Time up"
    }
}, 1000); // ინტერვალი შესრულდება ყოველ 1000 მილიწამში (1 წამი)
// clearInterval ფუნქცია გამოიყენება იმისთვის, რომ შეწყვიტოს ინტერვალი, რომელიც შექმნილია setInterval ფუნქციით. ამ შემთხვევაში, როდესაც დრო გახდება 0, ჩვენ ვაჩერებთ ინტერვალს, რათა აღარ გაგრძელდეს დროის დაკლება და კონსოლში ტექსტის გამოჩენა.