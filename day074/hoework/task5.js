let time = 0; // საწყისი დრო 0 წუთი

let minuteInterval = setInterval(function() {                       
    time++; // დრო გაიზრდება ერთით
    console.log("Current minutes: " + time); // კონსოლში გამოიტანეთ მიმდინარე წუთები
    if (time === 34) { // ვამოწმებთ თუ დრო გახდა 34
        clearInterval(minuteInterval); // clearInterval ფუნქცია აჩერებს ინტერვალს, რომელიც შექმნილია setInterval-ით
        console.log("Time up"); // კონსოლში გამოიტანეთ ტექსტი "Time up"
    }
}, 60000); // ინტერვალი შესრულდება ყოველ 60000 მილიწამში (1 წუთი)
// clearInterval ფუნქცია გამოიყენება იმისთვის, რომ შეწყვიტოს ინტერვალი, რომელიც შექმნილია setInterval ფუნქციით. პირობის if(time === 34) შემოწმება საჭიროა იმისთვის, რომ ინტერვალი გაჩერდეს, როდესაც დრო გახდება 34 წუთი, რათა აღარ გაგრძელდეს დროის ზრდა და კონსოლში ტექსტის გამოჩენა.