// 3) შექმენით User კომპონენტი, რომელიც მიიღებს props-ით name და age მნიშვნელობებს და გამოიტანს მათ <p> ელემენტებში. App.jsx-ში გამოიყენეთ User კომპონენტი და გადაეცით თქვენი სახელი და ასაკი. კომენტარებში დაწერეთ რას ნიშნავს props-ით მონაცემების გადაცემა
function User(props) {
    // props არის ობიექტი, რომელიც შეიცავს ყველა თვისებას (properties), რომლებიც გადაეცემა კომპონენტს
    return (
        <div>
            <p>Name: {props.name}</p> {/* აქ ჩვენ გამოვიყენებთ props-ის name თვისებას, რათა გამოვიტანოთ სახელი <p> ელემენტში */}
            <p>Age: {props.age}</p>   {/* აქ ჩვენ გამოვიყენებთ props-ის age თვისებას, რათა გამოვიტანოთ ასაკი <p> ელემენტში */}
        </div>
    );
}