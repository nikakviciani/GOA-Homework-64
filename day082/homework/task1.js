// 1) ახსენით როგორ მუშაობს  საიტერაციო ობიექტების მეთოდები: map, filter, forEach, reduce 

// 1) საიტერაციო ობიექტების მეთოდები:

// map: ქმნის ახალ მასივს, რომელიც შეიცავს იმ ელემენტებს, რომლებიც გადამუშავებულია მოცემული ფუნქციის საშუალებით.
const numbers = [1, 2, 3];
const doubled = numbers.map(num => num * 2); // [2, 4, 6]

// filter: ქმნის ახალ მასივს, რომელიც შეიცავს მხოლოდ იმ ელემენტებს, რომლებიც აკმაყოფილებენ მოცემულ პირობას.
const evens = numbers.filter(num => num % 2 === 0); // [2]

// forEach: ატარებს მოცემულ ფუნქციას თითოეულ ელემენტზე, მაგრამ არ ქმნის ახალ მასივს.
numbers.forEach(num => console.log(num)); // 1, 2, 3

// reduce: აკრიბებს ყველა ელემენტს ერთ მნიშვნელობაში, რომელიც შეიძლება იყოს ნებისმიერი ტიპი.
const sum = numbers.reduce((acc, num) => acc + num, 0); // 6